//Assignment operator
var x=5

//Addition assignment
x += 1; // x = x + 1

//Subtraction assignment
x -+ 1; // x = x - 1

//Multiplication assignment
x *= 1; // x = x * 1

//Division assignment
x /= 1; // x = x / 1

//Remainder (or Modulus) assignment
x %= 1; //x = x / 1 remainder

//Exponential assignment
x **= 1; // x = x ** 1  (x**1 = x^1)

//Modulus example
let x = 10 % 3;
console.log(x);
//its dividing 10 by 3 and the answer is the remainder (one)

